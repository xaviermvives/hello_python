print("Python has three numeric types: int, float, and complex") # muestra por pantalla una cadena texto

# creating a variable:
myValue = 1
# printamos la variable
print(myValue)
# mostramos el tipo de la variable:
print(type(myValue)) # <class 'int'>
# concatenamos numeros y texto con str() y +:
print(str(myValue) + " is of the data type " + str(type(myValue)))


# float type:
myValue= 3.14
# printamos la variable
print(myValue) # <class 'float'>
# mostramos el tipo de la variable:
print(type(myValue))
# concatenamos numeros y texto con str() y +:
print(str(myValue) + " is of the data type " + str(type(myValue)))



# complex data type
myValue=5j
# printamos la variable
print(myValue)
# mostramos el tipo de la variable:
print(type(myValue)) # <class 'complex'>
# concatenamos numeros y texto con str() y +:
print(str(myValue) + " is of the data type " + str(type(myValue)))



