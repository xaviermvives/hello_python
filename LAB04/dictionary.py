# a dictionary is a list with named positions (keys)

# we made a dictionary
myFavouriteFruitDictionary = {
    "Akua": "apple",
    "Saanvi": "banana",
    "Paulo": "pineapple"
}

print(myFavouriteFruitDictionary)
# {'Akua': 'apple', 'Saanvi': 'banana', 'Paulo': 'pineapple'}

print(type(myFavouriteFruitDictionary)) # <class 'dict'>

# accessing a dictionary by name:
print(myFavouriteFruitDictionary["Akua"]) # 'apple'
print(myFavouriteFruitDictionary["Saanvi"])
print(myFavouriteFruitDictionary["Paulo"])